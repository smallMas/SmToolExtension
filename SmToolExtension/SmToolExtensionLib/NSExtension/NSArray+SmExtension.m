- (NSString *)sm_toJsonString {
    NSError *error = nil;
    NSData *data = [NSJSONSerialization dataWithJSONObject:self options:kNilOptions error:&error];
    return [[NSString alloc] initWithData:data encoding:NSUTF8StringEncoding];
}

- (NSString *)getFirstLetterWithName:(NSString *)name {
    if (![SmUtility sm_objectIsNullAndEmpty:name]) {
        NSString *result = nil;
        unichar firstLetter = [name characterAtIndex:0];
        
        int index = firstLetter - HANZI_START;
        if (index >= 0 && index <= HANZI_COUNT) {
            result = [NSString stringWithFormat:@"%c", firstLetterArray[index]];
        } else if ((firstLetter >= 'a' && firstLetter <= 'z')
                   || (firstLetter >= 'A' && firstLetter <= 'Z')) {
            result = [NSString stringWithFormat:@"%c", firstLetter];
        } else {
            result = @"#";
        }
        return [result uppercaseString];
    }else {
        return @"#";
    }
}

- (NSArray *)sm_arrayWithPinYinFirstCharWithKey:(NSString *)key {
    if (![self count]) {
        return [NSMutableArray array];
    }
    
    NSMutableDictionary *dict = [NSMutableDictionary dictionary];
    [dict setObject:[NSMutableArray array] forKey:@"#"];
    for (int i = 'A'; i <= 'Z'; i++) {
        [dict setObject:[NSMutableArray array]
                 forKey:[NSString stringWithUTF8String:(const char *)&i]];
    }
    
    for (NSObject *ob in self) {
        NSString *name;
        if ([ob isKindOfClass:[NSString class]]) {
            name = (NSString *)ob;
        }else {
            //            name = [ob valueForKey:key];
            name = [self nameWithKey:key object:ob];
        }
        NSString *firstLetter = [self getFirstLetterWithName:name];
        NSMutableArray *array = dict[firstLetter];
        [array addObject:ob];
    }
    
    NSMutableArray *resultArray = [NSMutableArray array];
    for (int i = 'A'; i <= 'Z'; i++) {
        NSString *firstLetter = [NSString stringWithUTF8String:(const char *)&i];
        NSMutableArray *array = dict[firstLetter];
        if ([array count]) {
            [array sortUsingComparator:^NSComparisonResult(NSObject *obj1, NSObject* obj2) {
                //                NSString *word1 = [obj1 valueForKey:key];
                //                NSString *word2 = [obj2 valueForKey:key];
                NSString *word1 = [self nameWithKey:key object:obj1];
                NSString *word2 = [self nameWithKey:key object:obj2];
                return [word1 localizedCompare:word2];
            }];
//            NSDictionary *resultDict = @{SmFirstLetter: firstLetter,
//                                         SmContent: array};
//            [resultArray addObject:resultDict];
            SmPinYinModel *model = [SmPinYinModel sm_createFirst:firstLetter array:array];
            [resultArray addObject:model];
        }
    }
    
    if ([dict[@"#"] count]) {
        NSMutableArray *array = dict[@"#"];
        [array sortUsingComparator:^NSComparisonResult(NSObject *obj1, NSObject* obj2) {
            //            NSString *word1 = [obj1 valueForKey:key];
            //            NSString *word2 = [obj2 valueForKey:key];
            NSString *word1 = [self nameWithKey:key object:obj1];
            NSString *word2 = [self nameWithKey:key object:obj2];
            return [word1 localizedCompare:word2];
        }];
//        NSDictionary *resultDict = @{SmFirstLetter: @"#",
//                                     SmContent: array};
//        [resultArray addObject:resultDict];
        
        SmPinYinModel *model = [SmPinYinModel sm_createFirst:@"#" array:array];
        [resultArray addObject:model];
    }
    return resultArray;
}

- (NSString *)nameWithKey:(NSString *)key object:(NSObject *)ob {
    NSArray *keys = [key componentsSeparatedByString:@"."];
    if (keys.count == 1) {
        return (NSString *)[self nameKey:key object:ob];
    }else {
        NSObject *name = ob;
        for (NSString *k in keys) {
            name = [self nameKey:k object:name];
        }
        return (NSString *)name;
    }
}

- (NSObject *)nameKey:(NSString *)key object:(NSObject *)ob {
    return [ob valueForKey:key];
}


